#!/usr/bin/env bash
# lib/config.sh — User configuration management (setup.conf)
# Source this from scripts that need config values

SCRIPT_DIR_CONFIG="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
CONF_FILE="${SCRIPT_DIR_CONFIG}/setup.conf"
CONF_EXAMPLE="${SCRIPT_DIR_CONFIG}/setup.conf.example"

# ── Default values ──────────────────────────────────────────────
: "${PROJECTS_ROOT:="${HOME}/projects"}"
: "${NODE_VERSION:="22"}"
: "${INSTALL_BUN:="true"}"
: "${INSTALL_DOPPLER:="false"}"
: "${SECRETS_MANAGER:=""}"
: "${DOPPLER_PROJECT:=""}"
: "${DOPPLER_CONFIG:=""}"
: "${MCP_SERVERS:="memory,sequential-thinking,github,context7"}"
: "${PROJECT_DIR:=""}"

# ── Safe config parser (no source/eval — key=value only) ───────
_parse_config() {
  local file="$1"
  while IFS='=' read -r key value; do
    # Skip comments and blank lines
    [[ "$key" =~ ^[[:space:]]*# ]] && continue
    [[ -z "$key" ]] && continue
    # Trim whitespace
    key="$(echo "$key" | xargs)"
    # Strip surrounding quotes from value
    value="${value#\"}" ; value="${value%\"}"
    value="${value#\'}" ; value="${value%\'}"
    # Only allow known config keys
    case "$key" in
      PROJECTS_ROOT|NODE_VERSION|INSTALL_BUN|INSTALL_DOPPLER|\
      SECRETS_MANAGER|DOPPLER_PROJECT|DOPPLER_CONFIG|MCP_SERVERS|PROJECT_DIR)
        printf -v "$key" '%s' "$value"
        ;;
      *) echo "Warning: unknown config key '$key' ignored" >&2 ;;
    esac
  done < "$file"
}

# ── Load config ─────────────────────────────────────────────────
load_config() {
  if [ -f "$CONF_FILE" ]; then
    _parse_config "$CONF_FILE"
  fi
  # Expand ~ and $HOME in PROJECTS_ROOT
  PROJECTS_ROOT="${PROJECTS_ROOT/#\~/$HOME}"
  PROJECTS_ROOT="${PROJECTS_ROOT/\$HOME/$HOME}"
  # Resolve symlinks
  if [ -d "$PROJECTS_ROOT" ]; then
    PROJECTS_ROOT="$(cd "$PROJECTS_ROOT" && pwd)"
  fi
  # PROJECT_DIR: where to generate project-level files (.mcp.json, .code-workspace)
  # Defaults to current working directory if not set
  if [ -z "$PROJECT_DIR" ]; then
    PROJECT_DIR="$(pwd)"
  fi
  PROJECT_DIR="${PROJECT_DIR/#\~/$HOME}"
  PROJECT_DIR="${PROJECT_DIR/\$HOME/$HOME}"
}

# ── Migration detection ─────────────────────────────────────────
# Scans existing shell RC for marker blocks to infer config values
_detect_existing_config() {
  local rc_file="${1:-${HOME}/.bashrc}"

  # Check for Doppler wrapper
  if grep -q 'doppler run -- claude' "$rc_file" 2>/dev/null; then
    SECRETS_MANAGER="doppler"
  fi

  # Check for existing builds directory
  if [ -d "${HOME}/builds" ]; then
    PROJECTS_ROOT="${HOME}/builds"
  fi

  # Check if Bun is installed
  if command -v bun &> /dev/null; then
    INSTALL_BUN="true"
  fi

  # Check if Doppler is installed
  if command -v doppler &> /dev/null; then
    INSTALL_DOPPLER="true"
  fi
}

# ── Interactive config prompt ───────────────────────────────────
prompt_config() {
  echo "── Setup Configuration ──"
  echo ""
  echo "No setup.conf found. Let's create one."
  echo ""

  # Run migration detection for defaults
  _detect_existing_config

  read -rp "Projects root directory [${PROJECTS_ROOT}]: " input
  PROJECTS_ROOT="${input:-$PROJECTS_ROOT}"

  read -rp "Target project directory for .mcp.json and workspace [${PROJECT_DIR:-$PWD}]: " input
  PROJECT_DIR="${input:-${PROJECT_DIR:-$PWD}}"

  read -rp "Node.js major version [${NODE_VERSION}]: " input
  NODE_VERSION="${input:-$NODE_VERSION}"

  read -rp "Install Bun? (true/false) [${INSTALL_BUN}]: " input
  INSTALL_BUN="${input:-$INSTALL_BUN}"

  read -rp "Install Doppler CLI? (true/false) [${INSTALL_DOPPLER}]: " input
  INSTALL_DOPPLER="${input:-$INSTALL_DOPPLER}"

  if [[ "$INSTALL_DOPPLER" == "true" ]]; then
    read -rp "Use Doppler as secrets manager? (doppler or empty) [${SECRETS_MANAGER}]: " input
    SECRETS_MANAGER="${input:-$SECRETS_MANAGER}"
  fi

  echo ""
  echo "Available MCP servers: memory, sequential-thinking, github, context7, n8n-workflow-builder, n8n-mcp"
  read -rp "MCP servers to install (comma-separated) [${MCP_SERVERS}]: " input
  MCP_SERVERS="${input:-$MCP_SERVERS}"

  # Write config file with restricted permissions
  touch "$CONF_FILE"
  chmod 600 "$CONF_FILE"
  cat > "$CONF_FILE" << EOF
# setup.conf — Generated by setup-all.sh
# Edit this file to customize your setup, then re-run setup-all.sh

# Where your projects live (setup scans this for existing repos)
PROJECTS_ROOT="${PROJECTS_ROOT}"

# Node.js major version
NODE_VERSION="${NODE_VERSION}"

# Optional tools (true/false)
INSTALL_BUN="${INSTALL_BUN}"
INSTALL_DOPPLER="${INSTALL_DOPPLER}"

# Secrets manager: "doppler" or "" (empty = env vars only)
SECRETS_MANAGER="${SECRETS_MANAGER}"
DOPPLER_PROJECT="${DOPPLER_PROJECT}"
DOPPLER_CONFIG="${DOPPLER_CONFIG}"

# MCP servers to install (comma-separated short names)
# Available: memory, sequential-thinking, github, context7, n8n-workflow-builder, n8n-mcp
MCP_SERVERS="${MCP_SERVERS}"

# Target project directory (where .mcp.json and .code-workspace are generated)
# Defaults to current working directory if empty
PROJECT_DIR="${PROJECT_DIR}"
EOF

  echo ""
  echo "Config saved to: ${CONF_FILE}"
}

# ── Validate config ─────────────────────────────────────────────
validate_config() {
  # Expand ~ and $HOME
  PROJECTS_ROOT="${PROJECTS_ROOT/#\~/$HOME}"
  PROJECTS_ROOT="${PROJECTS_ROOT/\$HOME/$HOME}"

  # Validate NODE_VERSION is numeric
  if ! [[ "$NODE_VERSION" =~ ^[0-9]+$ ]]; then
    echo "Error: NODE_VERSION must be a number (e.g., 22), got: $NODE_VERSION" >&2
    return 1
  fi

  # Verify parent directory exists or can be created
  local parent
  parent="$(dirname "$PROJECTS_ROOT")"
  if [ ! -d "$parent" ]; then
    echo "Warning: parent directory $parent does not exist. It will be created."
  fi
}

# ── MCP package mapping ────────────────────────────────────────
# Maps short names to npm package names
# Usage: get_mcp_packages → prints one "short_name=package_name" per line
get_mcp_packages() {
  local IFS=','
  for server in $MCP_SERVERS; do
    server="$(echo "$server" | xargs)"  # trim whitespace
    case "$server" in
      memory)               echo "$server=@modelcontextprotocol/server-memory" ;;
      sequential-thinking)  echo "$server=@modelcontextprotocol/server-sequential-thinking" ;;
      github)               echo "$server=@modelcontextprotocol/server-github" ;;
      context7)             echo "$server=@upstash/context7-mcp" ;;
      n8n-workflow-builder) echo "$server=@makafeli/n8n-workflow-builder" ;;
      n8n-mcp)              echo "$server=n8n-mcp" ;;
      *)                    echo "Unknown MCP server: $server" >&2 ;;
    esac
  done
}
